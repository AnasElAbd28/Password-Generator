const passwordField1 = document.querySelector(".first-password");
const passwordField2 = document.querySelector(".second-password");

function generatePasswords() {
  let password1 = "";
  let password2 = "";
  function randomNumber() {
    let randomIndex = Math.floor(Math.random() * 90);
    return randomIndex;
  }
  for (let i = 0; i <= 15; i++) {
    password1 += characters[randomNumber()];
    password2 += characters[randomNumber()];
  }
  passwordField1.textContent = password1;
  passwordField2.textContent = password2;
}
